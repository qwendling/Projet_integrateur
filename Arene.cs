public class Arene{
	public static Arene[] arenes;


	private int id;
	private string nom;
	private string scene;
	public Arene(id,nom,scene){
		this.id = id;
		this.nom = nom;
		this.scene = scene;
	}

	public int getId(){
		return id;
	}

	public string getNom(){
		return nom;
	}

	public string getScene(){
		return scene;
	}




}
